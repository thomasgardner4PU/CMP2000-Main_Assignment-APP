public class BankData {
    private String name;
    private int age;
    private float balance;
    private boolean isPlatinumAccount;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public float getBalance() {
        return balance;
    }

    public void setBalance(float balance) {
        this.balance = balance;
    }

    public boolean isPlatinumAccount() {
        return isPlatinumAccount;
    }

    public void setPlatinumAccount(boolean isPlatinumAccount) {
        this.isPlatinumAccount = isPlatinumAccount;
    }
}
